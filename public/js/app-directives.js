appDirectives.directive('moveFocus', function() {
  return {
    restrict: 'A',
    link: function($scope,elem,attrs) {

      elem.bind('keyup', function(e) {

          var code = e.keyCode || e.which;
          console.log("Key was presssed "+code );

        if (code >= 48 && code <= 57) {
          e.preventDefault();
             elem.next().select();
             elem.next().focus();
        }
      });
    }
  }
});


